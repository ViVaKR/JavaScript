// 1. Arithmetic Operators

let x;
x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5;

// Concatenation
x = 'Hello' + ' ' + 'World';

// Exponent
x = 2 ** 3;

// Increment
x = 1;
x = x + 1;
x++;

// Decrement
x = x - 1;
x--;

// Assignment Operators
x = 10;
x += 5;
x = x + 5;
x -= 5;
x *= 5;
x /= 5;
x **= 5;

//3. Comparison Operators

x = 2 == '2'; // true, 내부 값만 평가
x = 2 === '2'; // false, 타입까지 평가
x = 2 != '2'; // false
x = 2 !== '2'; // true
x = 2 !== 2; // false
x = 10 > 5;
x = 10 < 5;
x = 10 <= 5;
x = 10 >= 5;

console.log(x);
